    private void btnSubirImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubirImagenActionPerformed
        // TODO add your handling code here:
        String destino;
        String origen;
        if(!txtID.getText().equals("")){
            idImg = "img" + txtID.getText() + ".jpg";
            SelectorArchivos chooser = new SelectorArchivos(null, true);
            chooser.setVisible(true);
            
            origen = chooser.path;
            String rutaFondo = System.getProperty("java.class.path")+ "\\img\\"+ idImg;
            String rut[] = rutaFondo.split(";");
            destino = rut[0];
            
           Path FROM = Paths.get(origen);
           Path TO = Paths.get(destino);
           // Sobre escribir el fichero de destino, si existe, y copiar los atributos,
           //incluyendo los permisos rwx
           
            CopyOption[] options = new CopyOption[]{
                StandardCopyOption.REPLACE_EXISTING,
                StandardCopyOption.COPY_ATTRIBUTES};
            
            try{
                Files.copy(FROM, TO.toAbsolutePath(), options);
                
            }catch(IOException ex){
                
            }
            ImageIcon Imagen = new ImageIcon(getClass().getResource("/img/"+ idImg));
            ImageIcon imgAjust = new ImageIcon(Imagen.getImage().getScaledInstance(
            lblImagen.getWidth(),lblImagen.getHeight(), Image.SCALE_DEFAULT));
            imgAjust.getImage().flush();
            lblImagen.setIcon(imgAjust);
            
        }else {
            JOptionPane.showMessageDialog(this, " Se debe establecer el id");
        }
    }//GEN-LAST:event_btnSubirImagenActionPerformed

