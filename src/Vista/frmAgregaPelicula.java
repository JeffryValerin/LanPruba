    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String valorTipo = "";
        if (!txtID.getText().equals("") && !txtTitulo.getText().equals("")
                && cmbGenero.getSelectedIndex() != 0 && cmbIdioma.getSelectedIndex() != 0
                && cmbClasificacion.getSelectedIndex() != 0 && (rdb2D.isSelected() || rdb3D.isSelected())) {
        if(!almacenaPelicula.yaExiste(Integer.parseInt(txtID.getText()))){        
            if (rdb2D.isSelected()) {
                    valorTipo = rdb2D.getText();

                } else if (rdb3D.isSelected()) {
                    valorTipo = rdb3D.getText();
                }
                Pelicula peliObj = new Pelicula(Integer.parseInt(txtID.getText()), txtTitulo.getText(),
                        String.valueOf(cmbGenero.getSelectedIndex()), Integer.parseInt(txtDuracion.getText()),
                        String.valueOf(cmbIdioma.getSelectedItem()), String.valueOf(cmbClasificacion.getSelectedItem()),
                        valorTipo, null);//Corregir ruta de imagen

                if (almacenaPelicula.insertarPelicular(peliObj)) {
                     JOptionPane.showMessageDialog(this, "Registro Almacenado");
                } else {
                    JOptionPane.showMessageDialog(this, "Vector lleno, borre registro");
                }
                this.dispose();
            }else{
                    JOptionPane.showMessageDialog(this, "El id ya existe");
                }
        }else{
            JOptionPane.showMessageDialog(this, "Faltan Datos");
        }

       

    
        
    }//GEN-LAST:event_btnGuardarActionPerformed

